#Navegação e Evacuação Espacial (Avaliação + Código 2)



def simular_evacucao():



  # Lista com os locais do trajeto

  locais = ["Quarto", "Corredor", "Sala", "Porta de Entrada", "Rua"]



  # Lista com o estado de cada local

  estados = ["caminho livre", "fumaça", "chave", "porta trancada", "seguro"]



  # Posição inicial do agente

  posicao = 0



  # Energia do agente

  energia = 8



  # Inventário

  tem_chave = False



  print("SIMULADOR DE EVACUAÇÃO")

  print("-" * 40)



  # Loop principal

  while energia > 0:



    local_atual = locais[posicao]

    estado_atual = estados[posicao]



    print("\nAgente está em:", local_atual)

    print("Estado do local:", estado_atual)

    print("Energia restante:", energia)



    # Se chegou ao destino final

    if local_atual == "Rua":

      print("\nO agente chegou à rua com segurança!")

      break



    # Verifica o estado do local

    if estado_atual == "caminho livre":

      print("O caminho está livre. O agente avança.")

      posicao += 1



    elif estado_atual == "fumaça":

      print("Há fumaça no local, mas o agente consegue passar com cuidado.")

      posicao += 1

      energia -= 1



    elif estado_atual == "chave":

      print("O agente encontrou uma chave.")

      tem_chave = True

      posicao += 1



    elif estado_atual == "porta trancada":

      print("A saída está trancada.")



      # If aninhado

      if tem_chave == True:

        print("O agente tem a chave e conseguiu abrir a porta.")

        posicao += 1

      else:

        print("O agente não tem a chave, então precisa voltar.")

        posicao -= 1

        energia -= 1



    elif estado_atual == "seguro":

      print("Local seguro.")

      posicao += 1



    # Garante que o agente não saia dos limites da lista

    if posicao < 0:

      posicao = 0



    if posicao >= len(locais):

      posicao = len(locais) - 1



    # Gasto básico de energia a cada tentativa

    energia -= 1


  # Caso a energia acabe

  if energia <= 0 and locais[posicao] != "Rua":

    print("\nO agente ficou sem energia antes de chegar à saída.")



simular_evacucao()

