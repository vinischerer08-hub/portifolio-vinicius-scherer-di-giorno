# Algoritmo do microclima local (Pesquisa + Código 1)



def analisar_microclima():



  # Lista de listas

  # [local, horario, temperatura, umidade, iqa]



  dados_locais = [

    ["Parque Linear Tiquatira", "08:00", 24, 96, 35],

    ["Parque Linear Tiquatira", "14:00", 29, 90, 42],

    ["Avenida Dom Helder Câmara", "08:00", 27, 60, 80],

    ["Avenida Dom Helder Câmara", "14:00", 33, 45, 115],

    ["Ponto de ônibus Jardim Popular", "08:00", 26, 95, 55],

    ["Ponto de ônibus Jardim Popular", "14:00", 34, 92, 95]

  ]



  print("ANÁLISE DO MICROCLIMA LOCAL")

  print("-" * 50)



  # Primeiro for

  for local in dados_locais:



    nome = local[0]

    horario = local[1]

    temperatura = local[2]

    umidade = local[3]

    iqa = local[4]



    # Classificação do IQA

    if iqa <= 40:

      faixa = 1

    elif iqa <= 80:

      faixa = 2

    elif iqa <= 120:

      faixa = 3

    else:

      faixa = 4



    match faixa:

      case 1:

        qualidade = "Boa"

      case 2:

        qualidade = "Moderada"

      case 3:

        qualidade = "Ruim"

      case 4:

        qualidade = "Muito Ruim"



    # Nota de conforto urbano

    nota = 10



    if temperatura > 30 or temperatura < 20:

      nota -= 2



    if umidade < 40:

      nota -= 2



    if iqa > 80:

      nota -= 3



    if nota < 0:

      nota = 0



    print("\nLocal:", nome)

    print("Horário:", horario)

    print("Temperatura:", temperatura, "°C")

    print("Umidade:", umidade, "%")

    print("IQA:", iqa)

    print("Qualidade do Ar:", qualidade)

    print("Nota de Conforto Urbano:", nota)



    # Segundo for (for aninhado)

    print("Valores registrados:")

    for valor in local:

      print(valor)





analisar_microclima()



