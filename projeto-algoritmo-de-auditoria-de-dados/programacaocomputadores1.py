def sistema_de_login():
  print("--- Portal Bancário: Acesso Restrito ---")

  # Variáveis de controle
  senha_cadastrada = "Python@2026" # Obs: na "vida real" essa informação é criptografada
  tentativas_falhas = 0
  limite_tentativas = 3
  acesso_liberado = False

  # while: (loop) Executa um bloco de código repetidamente 'enquanto uma condição for verdadeira'.
  # whie -> enquanto
  # and -> E inclusivo
  while tentativas_falhas < limite_tentativas and not acesso_liberado:
      senha_digitada = input(f"Tentativas {tentativas_falhas + 1} / {limite_tentativas} - Digite a senha:")
      if senha_digitada == senha_cadastrada:
          acesso_liberado = True
          print ("Acesso liberado!")
      else:
          # += como um "operador rápido" para incrementar 1
          tentativas_falhas += 1 # tentativas_falhas = tentativas_falhas + 1
          print ("Status: SENHA INCORRETA")
          # IF ANINHADO (Nested IF)
          if len(senha_digitada) > 8:
            # Só verifica o tamanho da senha se ela estiver errada
            print("A senha deve conter no minimo 8 caracteres.")
          if tentativas_falhas == limite_tentativas and senha_digitada != senha_cadastrada:
            print("Limite de tentativas excedido. Acesso bloqueado.")
            break

sistema_de_login()

