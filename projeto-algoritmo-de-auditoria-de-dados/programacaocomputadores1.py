from fastapi import FastAPI
from pydantic import BaseModel

app = FastAPI()

# lista que guarda as tarefas
tarefas = []

# modelo da tarefa
class Tarefa(BaseModel):
    titulo: str
    descricao: str
    concluida: bool = False


# rota inicial
@app.get("/")
def inicio():
    return {"mensagem": "API Organizador de Rotina"}


# listar tarefas
@app.get("/tarefas")
def listar_tarefas():
    return tarefas


# criar tarefa
@app.post("/tarefas")
def criar_tarefa(tarefa: Tarefa):
    tarefas.append(tarefa)
    return {"mensagem": "Tarefa criada com sucesso"}


# marcar tarefa como concluída
@app.put("/tarefas/{id}")
def concluir_tarefa(id: int):
    tarefas[id].concluida = True
    return {"mensagem": "Tarefa concluída"}


# deletar tarefa
@app.delete("/tarefas/{id}")
def deletar_tarefa(id: int):
    tarefas.pop(id)
    return {"mensagem": "Tarefa removida"}