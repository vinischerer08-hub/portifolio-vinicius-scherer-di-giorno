def prever_pegada_de_carbono():
  print("--- Simulador ambiental de data centers ---")

  # Entrada de Dados
  matriz_energetica = input("Escolha a fonte de energia (solar, eolica, carvão, gas)").lower()

  mese_projecao = int(input("Digite a quantidade de meses para a projeção: "))

  consumo_base_kwh = 1000.0 # Consumo inicial no mês zero

  taxa_crescimento_mensal = 0.05 # O consumo crsce 5% ao mês devido a novos usuários

  match matriz_energetica:
    # O caractere | significa 'ou' da tabela verdade
    case "solar" | "eolica":
      fator_emisao = 0.04
      print("-> Fonte renovavel detectada. ")
    case "gas":
      fator_emisao = 0.40
      print("-> Fator de transição detectada. ")
    case "carvao":
      fator_emisao = 1.20
      print("-> ATENÇÃO: Fonte altamente poluente detectada ")
    case _: # O caso underline é o default, caso os usuários digitem algo diferente
      fator_emisao = 0.50
      print("-> Fonte desconhecida. Usando a média global.")

  emissao_acumulada = 0
  # Estrutura de repetição do FOR
  for mes in range(1, mese_projecao + 1):
    # Matemática: (Juros compostos)
    #Montante = capital * (1 + taxa) ** tempo
    consumo_projetado = consumo_base_kwh * ((1 + taxa_crescimento_mensal) ** mes)

    # Emissão daquele mes especifico
    emissao_mes = consumo_projetado * fator_emisao
    emissao_acumulada += emissao_mes #Operador de incremento

    print(f"Mês {mes}: Consumo = {consumo_projetado:.2f} KWh, Emissao = {emissao_mes:.2f} kg de CO2")

  print(f"\nResumo: em {mese_projecao} meses, o total emitido de {emissao_acumulada:.2f} kg de CO2")

prever_pegada_de_carbono()

